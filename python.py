hello_python = "hello python"
# 파이썬은 기본적으로 변수를 선언하는 키워드가 없다.
# 자바스크립트에서는 var, let, const등 버전에 따라 성격에 따라 다양한 변수 선언 방식이 존재하는 점과 대조적이다.
# 파이썬은 태생이 런타임(실행환경) 기반인 것이 큰 특징이다.
# 다시말해, node.js도 런타임 python도 런타임이다.

# 좀 더 전문적으로 말하면
# 파이썬은 변수 선언조차 동적(dynamic)으로 결정하기 때문에, 상당히 간결한 작성환경을 제공한다.
# node.js가 할 수 있는 일은 python도 동일하게 할 수 있다.

print(hello_python) 
# 자바스크립트에서는 console.log()로 로깅하는 방식을 채택했지만, 파이썬은 함수형태로 지원한다.